public class Opcion {
    private int opcId;
    private String opcDes;
    private String opcLnk;
    private String opcEst;

    public int getOpcId() {
        return opcId;
    }

    public void setOpcId(int opcId) {
        this.opcId = opcId;
    }

    public String getOpcDes() {
        return opcDes;
    }

    public void setOpcDes(String opcDes) {
        this.opcDes = opcDes;
    }

    public String getOpcLnk() {
        return opcLnk;
    }

    public void setOpcLnk(String opcLnk) {
        this.opcLnk = opcLnk;
    }

    public String getOpcEst() {
        return opcEst;
    }

    public void setOpcEst(String opcEst) {
        this.opcEst = opcEst;
    }
    
    
    
}
